    private void txtAnioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAnioKeyTyped
        char c = evt.getKeyChar();
        String text = txtAnio.getText();

        // Validar que solo se ingresen nÃºmeros y que no exceda la longitud permitida
        if (!Character.isDigit(c) || text.length() >= 4) {
            evt.consume(); // Ignorar la entrada si no es un dÃ­gito o si excede la longitud
            return;
        }

        // Concatenar el dÃ­gito ingresado al texto existente en el campo
        text += c;

        try {
            int year = Integer.parseInt(text);
            if (year > 2023) {
                showMessageDialog(null, "El aÃ±o no puede ser mayor a 2023.");
                 // Establecer automÃ¡ticamente el aÃ±o a 2023 si es mayor
            }
        } catch (NumberFormatException ex) {
            // Manejar la excepciÃ³n si no se puede convertir a nÃºmero
            showMessageDialog(null, "Ingresa un aÃ±o vÃ¡lido.");
            
        }
    }//GEN-LAST:event_txtAnioKeyTyped

